# ==========================================================
#                  VTOL PARAMETERS
# ==========================================================

class VTOLParameters:

    def __init__(self):

        # ==================================================
        #                    MASS
        # ==================================================

        self.mass = 1.5

        # ==================================================
        #                  GRAVITY
        # ==================================================

        self.gravity = 9.81

        # ==================================================
        #                ARM LENGTH
        # ==================================================
        #
        # Distance from CoG to motor
        #
        # ==================================================

        self.arm_length = 0.25

        # ==================================================
        #              MOMENTS OF INERTIA
        # ==================================================
        #
        # About body frame axes
        #
        # ==================================================

        self.Ixx = 0.03

        self.Iyy = 0.03

        self.Izz = 0.05

        # ==================================================
        #          PROPELLER ROTATIONAL INERTIA
        # ==================================================
        #
        # Used in gyroscopic coupling
        #
        # ==================================================

        self.JTP = 0.00012

        # ==================================================
        #            THRUST COEFFICIENT
        # ==================================================
        #
        # T = CT * Ω²
        #
        # ==================================================

        self.CT = 0.00005

        # ==================================================
        #             DRAG COEFFICIENT
        # ==================================================
        #
        # Q = CQ * Ω²
        #
        # ==================================================

        self.CQ = 0.000001

        # ==================================================
        #             MOTOR TIME CONSTANT
        # ==================================================
        #
        # For actuator lag model
        #
        # ==================================================

        self.motor_tau = 0.05

        # ==================================================
        #             MAX MOTOR SPEED
        # ==================================================

        self.max_omega = 1200.0

        # ==================================================
        #             MIN MOTOR SPEED
        # ==================================================

        self.min_omega = 0.0

        # ==================================================
        #               SAMPLE TIME
        # ==================================================

        self.dt = 0.01

        # ==================================================
        #          AERODYNAMIC DRAG TERMS
        # ==================================================
        #
        # Placeholder for transition model
        #
        # ==================================================

        self.drag_u = 0.1

        self.drag_v = 0.1

        self.drag_w = 0.2

        # ==================================================
        #           TRANSITION PARAMETERS
        # ==================================================

        self.wing_area = 0.35

        self.air_density = 1.225

        self.CL_alpha = 5.5

        self.CD_0 = 0.02

        # ==================================================
        #           CONTROL LIMITS
        # ==================================================

        self.max_roll_torque = 5.0

        self.max_pitch_torque = 5.0

        self.max_yaw_torque = 2.0

        self.max_total_thrust = 60.0