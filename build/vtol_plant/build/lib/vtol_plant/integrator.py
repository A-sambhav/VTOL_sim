import numpy as np


# ==========================================================
#                    RK4 INTEGRATOR
# ==========================================================

class RK4Integrator:

    def __init__(

        self,
        dynamics,
        dt

    ):

        # ==================================================
        #                 DYNAMICS MODEL
        # ==================================================

        self.dynamics = dynamics

        # ==================================================
        #                  SAMPLE TIME
        # ==================================================

        self.dt = dt

    # ======================================================
    #                   RK4 STEP
    # ======================================================

    def step(

        self,
        X,
        U

    ):

        # ==================================================
        #                      K1
        # ==================================================

        k1 = self.dynamics.state_derivative(

            X,
            U

        )

        # ==================================================
        #                      K2
        # ==================================================

        k2 = self.dynamics.state_derivative(

            X + (self.dt / 2.0) * k1,
            U

        )

        # ==================================================
        #                      K3
        # ==================================================

        k3 = self.dynamics.state_derivative(

            X + (self.dt / 2.0) * k2,
            U

        )

        # ==================================================
        #                      K4
        # ==================================================

        k4 = self.dynamics.state_derivative(

            X + self.dt * k3,
            U

        )

        # ==================================================
        #                RK4 INTEGRATION
        # ==================================================

        X_next = (

            X

            + (self.dt / 6.0)

            * (

                k1
                + 2.0 * k2
                + 2.0 * k3
                + k4

            )

        )

        return X_next