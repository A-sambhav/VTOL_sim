# ==========================================================
#                    IMPORTS
# ==========================================================

import numpy as np

import math


# ==========================================================
#                  CONTROL ALLOCATOR
# ==========================================================

class ControlAllocator:

    def __init__(self, params):

        # ==================================================
        #                  PARAMETERS
        # ==================================================

        self.CT = params.CT

        self.CQ = params.CQ

        self.l = params.arm_length

        # ==================================================
        #           EFFECTIVE X CONFIG ARM
        # ==================================================

        self.le = (

            self.l /
            math.sqrt(2.0)

        )

        # ==================================================
        #              ALLOCATION MATRIX
        # ==================================================
        #
        # U = A * Ω²
        #
        # ==================================================

        self.A = np.array([

            # ------------------------------------------
            # U1 : TOTAL THRUST
            # ------------------------------------------

            [

                self.CT,
                self.CT,
                self.CT,
                self.CT

            ],

            # ------------------------------------------
            # U2 : ROLL TORQUE
            # ------------------------------------------

            [

                self.le * self.CT,
                self.le * self.CT,
                -self.le * self.CT,
                -self.le * self.CT

            ],

            # ------------------------------------------
            # U3 : PITCH TORQUE
            # ------------------------------------------

            [

                -self.le * self.CT,
                self.le * self.CT,
                self.le * self.CT,
                -self.le * self.CT

            ],

            # ------------------------------------------
            # U4 : YAW TORQUE
            # ------------------------------------------

            [

                self.CQ,
                -self.CQ,
                self.CQ,
                -self.CQ

            ]

        ])

        # ==================================================
        #             INVERSE ALLOCATION MATRIX
        # ==================================================

        self.A_inv = np.linalg.inv(self.A)

    # ======================================================
    #                 COMPUTE MOTOR SPEEDS
    # ======================================================

    def allocate(

        self,
        U1,
        U2,
        U3,
        U4

    ):

        # ==================================================
        #                CONTROL VECTOR
        # ==================================================

        U = np.array([

            U1,
            U2,
            U3,
            U4

        ])

        # ==================================================
        #                 COMPUTE Ω²
        # ==================================================

        omega_squared = (

            self.A_inv @ U

        )

        # ==================================================
        #          PREVENT NEGATIVE MOTOR VALUES
        # ==================================================

        omega_squared = np.clip(

            omega_squared,
            0.0,
            None

        )

        # ==================================================
        #                 COMPUTE Ω
        # ==================================================

        omega = np.sqrt(

            omega_squared

        )

        # ==================================================
        #             MOTOR SPEED SATURATION
        # ==================================================

        omega = np.clip(

            omega,
            0.0,
            1200.0

        )

        # ==================================================
        #                 RETURN VALUES
        # ==================================================

        return omega